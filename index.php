<script>
// كود قائمة الجوال - مباشر وبسيط
document.addEventListener('DOMContentLoaded', function() {
    console.log('بدء تحميل قائمة الجوال...');
    
    const menuBtn = document.getElementById('mobileMenuBtn');
    const menu = document.getElementById('mobileMenu');
    const icon = document.getElementById('menuIcon');
    
    console.log('العناصر:', {menuBtn, menu, icon});
    
    if (menuBtn && menu && icon) {
        console.log('تم العثور على جميع عناصر القائمة');
        
        // إخفاء القائمة افتراضياً
        menu.classList.remove('active');
        
        menuBtn.addEventListener('click', function(e) {
            e.preventDefault();
            e.stopPropagation();
            console.log('تم النقر على زر القائمة');
            
            menu.classList.toggle('active');
            
            if (menu.classList.contains('active')) {
                icon.className = 'fas fa-times';
                console.log('فتح القائمة');
            } else {
                icon.className = 'fas fa-bars';
                console.log('إغلاق القائمة');
            }
        });
        
        // إغلاق القائمة عند النقر على رابط
        const navLinks = menu.querySelectorAll('.nav-link');
        navLinks.forEach(link => {
            link.addEventListener('click', function() {
                console.log('نقر على رابط، إغلاق القائمة');
                menu.classList.remove('active');
                icon.className = 'fas fa-bars';
            });
        });
        
        // إغلاق القائمة عند النقر خارجها
        document.addEventListener('click', function(e) {
            if (menu.classList.contains('active') && 
                !menu.contains(e.target) && 
                !menuBtn.contains(e.target)) {
                console.log('نقر خارج القائمة، إغلاقها');
                menu.classList.remove('active');
                icon.className = 'fas fa-bars';
            }
        });
        
        console.log('تم تهيئة قائمة الجوال بنجاح');
    } else {
        console.error('عناصر القائمة مفقودة:', {menuBtn, menu, icon});
    }
});
</script>
